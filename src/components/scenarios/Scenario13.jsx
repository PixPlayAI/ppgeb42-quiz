      // Limpa o canvas
      ctx.clearRect(0, 0, canvas.width, canvas.height);

      // Fundo
      ctx.fillStyle = isDark ? '#1f2937' : '#e5e7eb';
      ctx.fillRect(0, 0, canvas.width, canvas.height);

      // TÃ­tulo
      ctx.fillStyle = isDark ? '#e5e7eb' : '#1f2937';
